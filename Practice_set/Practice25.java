// Assignment Operators (%=)

public class Practice25 {
  public static void main(String args[]) {
    int A = 5, B = 5 ;
    System.out.println("Value of A = "+A+"\nValue of B = "+B);
    A = A % 3;
    System.out.println("\nA = A % 3\nA = "+A);
    B %= 3 ;
    System.out.println("\nB %= 3\nB = "+B);
  }
}
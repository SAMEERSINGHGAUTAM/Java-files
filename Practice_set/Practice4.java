//  Unary Operators
public class Practice4 {
  public static void main(String args[]) {
    int num = 56;

    System.out.println("The value of num: "+ num);
    num++; // value is used and incremented
    System.out.println("The value of num: "+num);
    System.out.println("The value of num++: "+ num++);
    System.out.println("The value of num: "+ num);
    System.out.println("The value of ++num: "+ ++num);
  }
}